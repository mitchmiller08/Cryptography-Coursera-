import gmpy2

def calculate(p,g,h):
	B = gmpy2.mpz(2**20)
	p = gmpy2.mpz(p)
	g = gmpy2.mpz(g)
	h = gmpy2.mpz(h)
	
	table = {}
	
	for x1 in range(B):
		table[gmpy2.divm(h, gmpy2.powmod(g,x1,p),p)] = x1
	
	for x0 in range(B):
		value = gmpy2.powmod(g, B*x0, p)
		if value in table:
			x1 = table[value]
			return B*x0+x1
	
	return None
	
	
